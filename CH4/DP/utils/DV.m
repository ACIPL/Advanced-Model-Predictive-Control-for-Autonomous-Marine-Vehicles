function [ D ] = DV( vel )

% System coefficients
%==========================================================================
Xu = 26.9;
Yv = 35.8;
Nr = 3.5;
Du = 241.3;
Dv = 503.8;
Dr = 76.9;
%==========================================================================

u = vel(1);
v = vel(2);
r = vel(3);
D = diag([-Xu-Du*abs(u);-Yv-Dv*abs(v);-Nr-Dr*abs(r)]);
D = -D;

end

